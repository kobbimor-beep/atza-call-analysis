    lines.append("ניגירי וסאשימי: " + ", ".join(ATZA_NIGIRI.keys()))
    lines.append("סושי סנדוויץ': " + ", ".join(ATZA_SANDWICHES.keys()))
    lines.append("קומבינאצות: " + ", ".join(ATZA_COMBOS.keys()))
    lines.append("פוקי: " + ", ".join(ATZA_POKE.keys()))
    lines.append("נודלס: " + ", ".join(ATZA_NOODLES.keys()))
    lines.append("מוקפץ אורז: " + ", ".join(ATZA_RICE.keys()))
    lines.append("באנים: " + ", ".join(ATZA_BAO.keys()))
    lines.append("נשנושים: " + ", ".join(ATZA_SNACKS.keys()))
    lines.append("תוספות: " + ", ".join(ATZA_SIDES.keys()))
    lines.append("סניפים: " + ", ".join(ATZA_BRANCHES))
    return "\n".join(lines)
